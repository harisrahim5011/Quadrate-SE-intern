
// TypeScript infers 'number' because it's initialized with a numeric literal
let count = 10;

// TypeScript infers 'string' due to the string literal initialization
let message = "Hello, TypeScript!";

// TypeScript infers 'boolean' from the boolean literal assignment
let isActive = true;

// TypeScript infers 'number[]' since all array elements are numbers
let scores = [85, 92, 78, 90];

// TypeScript infers specific object type: { id: number, username: string }
let userProfile = { id: 1, username: "coder123" };